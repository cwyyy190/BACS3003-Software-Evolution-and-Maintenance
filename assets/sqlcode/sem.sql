use semdb
;


DROP TABLE IF EXISTS `participants`;
DROP TABLE IF EXISTS `events`;
DROP TABLE IF EXISTS `organizer`;

CREATE TABLE `organizer` (
  `organizer_id` varchar(20) NOT NULL,
  `organizer_name` varchar(50) DEFAULT NULL,
  `description` varchar(100) DEFAULT NULL,
  `email` varchar(50) DEFAULT NULL,
  `phone` varchar(15) DEFAULT NULL,
  `orgIMG_url` varchar(255) DEFAULT NULL,
  PRIMARY KEY (`organizer_id`)
) ;

CREATE TABLE `events` (
  `event_id` varchar(20) NOT NULL,
  `event_name` varchar(50) DEFAULT NULL,
  `overview` varchar(500) DEFAULT NULL,
  `desc1` varchar(500) DEFAULT NULL,
  `desc2` varchar(500) DEFAULT NULL,
  `learnTitle1` varchar(200) DEFAULT NULL,
  `learnTitle2` varchar(200) DEFAULT NULL,
  `learnTitle3` varchar(200) DEFAULT NULL,
  `learnTitle4` varchar(200) DEFAULT NULL,
  `learnDesc1` varchar(500) DEFAULT NULL,
  `learnDesc2` varchar(500) DEFAULT NULL,
  `learnDesc3` varchar(500) DEFAULT NULL,
  `learnDesc4` varchar(500) DEFAULT NULL,
  `wayToJoin` varchar(500) DEFAULT NULL,
  `event_date` date DEFAULT NULL,
  `event_time` varchar(10) DEFAULT NULL,
  `location` varchar(100) DEFAULT NULL,
  `available` varchar(20) DEFAULT NULL,
  `eduLevel` varchar(20) DEFAULT NULL,
  `price` decimal(10,2) DEFAULT '0.00',
  `duration_hours` int DEFAULT NULL,
  `participants_count` int DEFAULT NULL,
  `eventIMG_url` varchar(255) DEFAULT NULL,
  `category` varchar(100) DEFAULT NULL,
  `organizer_id` varchar(10) DEFAULT NULL,
  PRIMARY KEY (`event_id`),
  KEY `fk_organizer` (`organizer_id`),
  CONSTRAINT `fk_organizer` FOREIGN KEY (`organizer_id`) REFERENCES `organizer` (`organizer_id`)
) ;
CREATE TABLE `participants` (
  `participant_id` varchar(20) NOT NULL,
  `first_name` varchar(100) DEFAULT NULL,
  `last_name` varchar(100) DEFAULT NULL,
  `studentID` varchar(20) DEFAULT NULL,
  `email` varchar(100) DEFAULT NULL,
  `phone` varchar(15) DEFAULT NULL,
  `facultyStudent` varchar(3) DEFAULT NULL,
  `media` varchar(50) DEFAULT NULL,
  `event_id` varchar(20) DEFAULT NULL,
  PRIMARY KEY (`participant_id`),
  KEY `fk_event_id` (`event_id`),
  CONSTRAINT `fk_event_id` FOREIGN KEY (`event_id`) REFERENCES `events` (`event_id`)
) ;

